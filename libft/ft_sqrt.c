#include "libft.h"

int	ft_sqrt(int n)
{
	int	i;

	if (n < 4)
		return (1);
	i = 2;
	while (i * i < n)
		i++;
	if (i * i > n)
	{
		if ((i * i - n) < ((i - 1) * (i - 1) + (-n)))
			return (i);
	}
	return (i - 1);
}
